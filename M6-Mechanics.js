    nextBtn.className = 'choice-btn';
    nextBtn.style.marginTop = '2rem';
    nextBtn.style.opacity = '0';
    nextBtn.innerHTML = state.currentScene < sc.scenes.length - 1 ? '继续' : '查看结局';
